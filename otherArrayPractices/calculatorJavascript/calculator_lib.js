let sumOfNumbers = function(firstNumber,secondNumber){
  return (firstNumber + secondNumber);
}
exports.sum = sumOfNumbers;
let differenceOfNumbers = function(firstNumbr,secondNumber){
  return (firstNumbr - secondNumber);
}
exports.difference = differenceOfNumbers;

let productOfNumbers = function(firstNumbr,secondNumber){
  return (firstNumbr * secondNumber);
}
exports.product = productOfNumbers;
let quotientOfNumbers = function(firstNumbr,secondNumber){
  return (firstNumbr / secondNumber);
}
exports.quotient = quotientOfNumbers;
